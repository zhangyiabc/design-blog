<template>
  <div class="login-container">
    <el-form
      ref="loginForm"
      :model="loginForm"
      :rules="loginRules"
      class="login-form"
      auto-complete="on"
      label-position="left"
    >
      <div class="title-container">
        <h3 class="title">多人博客后台系统</h3>
      </div>

      <el-form-item prop="username">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          ref="username"
          v-model="loginForm.username"
          placeholder="请输入管理员账号"
          name="username"
          type="text"
          tabindex="1"
          auto-complete="on"
        />
      </el-form-item>

      <el-form-item prop="password">
        <span class="svg-container">
          <svg-icon icon-class="password" />
        </span>
        <el-input
          :key="passwordType"
          ref="password"
          v-model="loginForm.password"
          :type="passwordType"
          placeholder="请输入管理员密码"
          name="password"
          tabindex="2"
          auto-complete="on"
          @keyup.enter.native="handleLogin"
        />
        <span class="show-pwd" @click="showPwd">
          <svg-icon
            :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'"
          />
        </span>
      </el-form-item>
      <div class="captcha-container">
        <el-form-item prop="captcha" class="captcha-input">
          <span class="svg-container">
            <!-- <svg-icon icon-class="password" /> -->
            <i class="el-icon-set-up" />
          </span>
          <el-input
            v-model="captcha"
            placeholder="请输入验证码"
            name="captcha"
            tabindex="3"
            auto-complete="on"
          />
        </el-form-item>
        <div class="captcha-img" @click="handleChangeCaptcha" v-html="svg" />
      </div>

      <el-button
        :loading="loading"
        type="primary"
        style="width: 100%; margin-bottom: 30px"
        @click.native.prevent="handleLogin"
      >登录</el-button>

      <!-- <div class="tips">
        <span style="margin-right:20px;">username: admin</span>
        <span> 看不清？点击验证码更换</span>
      </div> -->
    </el-form>
  </div>
</template>

<script>
import { getCaptcha } from '@/api/captcha'
import Cookies from 'js-cookie'
export default {
  name: 'Login',
  data() {
    const validatePassword = (rule, value, callback) => {
      if (value.length < 6) {
        callback(new Error('The password can not be less than 6 digits'))
      } else {
        callback()
      }
    }
    return {
      svg: '',
      loginForm: {
        username: '',
        password: ''
      },
      captcha: '',
      loginRules: {
        username: [
          {
            required: true,
            trigger: 'blur'
          },
          { min: 6, max: 10, message: '长度在 6 到 10 个字符', trigger: 'blur' }
        ],
        password: [
          {
            required: true,
            trigger: 'blur',
            validator: validatePassword
          }
        ]
      },
      loading: false,
      passwordType: 'password',
      redirect: undefined
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        this.redirect = route.query && route.query.redirect
      },
      immediate: true
    }
  },
  mounted() {
    this.handleChangeCaptcha()
  },
  methods: {
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    handleLogin() {
      // 验证 验证码
      const captcha = Cookies.get('captcha')
      if (this.captcha.toLocaleUpperCase() !== captcha.toLocaleUpperCase()) {
        this.$message({
          type: 'error',
          message: '验证码错误！'
        })
        this.handleChangeCaptcha()
        return
      }
      this.$refs.loginForm.validate((valid) => {
        if (valid) {
          this.loading = true
          this.$store
            .dispatch('user/login', this.loginForm)
            .then(() => {
              console.log(this.redirect)
              this.$router.push({
                path: this.redirect || '/'
              })
              this.loading = false
            })
            .catch(() => {
              this.loading = false
            })
        } else {
          console.log('error submit!!')
          return false
        }
      })
    },
    handleChangeCaptcha() {
      getCaptcha().then((res) => {
        this.svg = res.data
      })
    }
  }
}
</script>

